<?php   // inc.php

$counter = 0;
echo "cnt: {$counter} <br>";

$counter = $counter + 1;
echo "cnt: {$counter} <br>";

$counter += 1;
echo "cnt: {$counter} <br>";

// 加算子（インクリメント
$counter ++; // 後置
echo "cnt: {$counter} <br>";

++ $counter; // 前置
echo "cnt: {$counter} <br>";

// 減算子（デクリメント
$counter --; // 後置
echo "cnt: {$counter} <br>";

-- $counter; // 前置
echo "cnt: {$counter} <br>";






