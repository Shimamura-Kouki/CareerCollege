<?php  // val.php

// 四則演算
$i = 10;
$j = 7;
echo  "{$i} <br>\n";
echo  "{$j} <br>\n";

// 足し算
$k = $i + $j;
echo "{$i} + {$j} = {$k} <br>\n";

// 引き算
$k = $i - $j;
echo "{$i} - {$j} = {$k} <br>\n";

// 掛け算
$k = $i * $j;
echo "{$i} * {$j} = {$k} <br>\n";

// 割り算
$k = $i / $j;
echo "{$i} / {$j} = {$k} <br>\n";

