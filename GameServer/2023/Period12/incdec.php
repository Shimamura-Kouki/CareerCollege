<?php  // incdec.php

$num = 0;
echo $num;
echo "<br>";

$num = $num + 1;
echo $num;
echo "<br>";

$num += 1;
echo $num;
echo "<br>";

// インクリメント演算子
$num ++; // 後置
echo $num;
echo "<br>";

++ $num; // 前置
echo $num;
echo "<br>";

// デクリメント演算子
-- $num; // 前置
echo $num;
echo "<br>";

$num --; // 後置
echo $num;
echo "<br>";

$num -= 1;
echo $num;
echo "<br>";
