<?php  // inc.php

$num = 0;
echo "num is {$num} <br>";

// $num = $num + 1;
$num += 1;
echo "num is {$num} <br>";

// 加算子（インクリメント)
$num ++; // 後置
echo "num is {$num} <br>";

++ $num; // 前置
echo "num is {$num} <br>";

// 減算子（デクリメント)
$num --; // 後置
echo "num is {$num} <br>";

-- $num; // 前置
echo "num is {$num} <br>";


